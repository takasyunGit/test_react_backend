class AvatarUploader < CarrierWave::Uploader::Base
  # Include RMagick or MiniMagick support:
  # include CarrierWave::RMagick
  include CarrierWave::MiniMagick

  # Choose what kind of storage to use for this uploader:
  if Rails.env.development?
    storage :file
  elsif Rails.env.test?
    storage :file
  else
    storage :fog
  end

  # Override the directory where uploaded files will be stored.
  # This is a sensible default for uploaders that are meant to be mounted:
  # Userモデル以外がavatarを参照するときも使用するため条件分岐
  # detail carrierwave/storage/file.rb #retrieve!
  def store_dir
    if model.class == VendorOfferChat
      if model.user_id
        "uploads/user/#{mounted_as}/#{model.user_id}"
      else
        "uploads/vendor_user/#{mounted_as}/#{model.vendor_user_id}"
      end
    elsif model.class == VendorOffer
      "uploads/vendor_user/#{mounted_as}/#{model.vendor_user_id}"
    elsif model.class == UserOffer
      "uploads/user/#{mounted_as}/#{model.user_id}"
    else
      "uploads/#{model.class.to_s.underscore}/#{mounted_as}/#{model.id}"
    end
  end

  # Provide a default URL as a default if there hasn't been a file uploaded:
  # def default_url(*args)
